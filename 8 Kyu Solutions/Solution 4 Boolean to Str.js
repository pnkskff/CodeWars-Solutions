// Complete the method that takes a boolean value and return a "Yes" string for true, or a "No" string for false.

// Parameters: Boolean Value, String
// Return: Return 'Yes' Str for True and 'No' Str for False
// Example: Yes turns to 'Yes', No turns to 'No'
// Pseudocode:
// 1. Create Function
// 2. Return Bool (True or False) as String (Yes or No)
// 3. Call Function

function boolToWord(bool) {
  return bool ? 'Yes' : 'No'
}

