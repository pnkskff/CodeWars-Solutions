// 24. Double an Integer and Return
// You need to double the integer and return it..

// Parameters: Integer
// Return: Double given Integer
// Example: 2 turns to 4
// Pseudocode:
// 1. Create Function
// 2. Use * Operator to double Integer, see below
// https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Operators/Multiplication
// 3. Call Function

function doubleInteger(i) {
  return i * 2
}

doubleInteger(2)
